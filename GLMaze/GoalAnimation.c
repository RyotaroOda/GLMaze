//この関数を起動したらアニメーション始動
void BoxAnimationTrigger(int value){
    animation = true;
    box_place[0] = mainCameraPosition.x;
    box_place[1] = mainCameraPosition.y + 0.45;
    box_place[2] = 0.3;
    if (!anime_flg){
        box_theta = 0;
        gv = 0.2; // initial velocity
        anime_flg = 1;
        srand((unsigned int)time(NULL));
        chara_rand = rand() % 4;
        goalAnimation();
        glutPostRedisplay();
        glLoadIdentity();
        glutTimerFunc(10, BoxAnimationTrigger, 0);
    }
    if (anime_flg){
        if(gv > 0){
            box_theta += gv;
            gv -= 0.01; // accelaration rate
            glutPostRedisplay();
            glLoadIdentity();
            glutTimerFunc(10, BoxAnimationTrigger, 0);
        }else{
            //gv = 0;
            //anime_flg = 0;
            animation = false;
        }
    }
}

